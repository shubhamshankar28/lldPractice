package org.shubhamshankar;

public class InputCoordinate {
    private final Integer x;
    private final Integer y;

    public InputCoordinate(Integer x, Integer y) {
        this.x = x;
        this.y = y;
    }

    public Integer getX() {
        return x;
    }

    public Integer getY() {
        return y;
    }
}
